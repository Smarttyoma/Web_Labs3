<?php
// Задаем любое число
$myNum = 10;

// Копируем значение в $answer
$answer = $myNum;

// Добавляем 2
$answer += 2;

// Умножаем на 2
$answer *= 2;

// Вычитаем 2
$answer -= 2;

// Делим на 2
$answer /= 2;

// Вычитаем исходное число
$answer -= $myNum;

// Выводим результат
echo $answer;
?>
